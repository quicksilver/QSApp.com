    				<h2>What about Spotlight for Mac OS X?</h3> 
				<a name="spotlight"></a>
	    			Spotlight is fantastic and it certainly has its place, but it&#8217;s no substitute for Quicksilver. 
	    			<ul> 
	      			<li>Spotlight knows about <b>everything</b> on your Mac. Quicksilver also knows about everything, but concentrates on the things you care about.</li> 
	      			<li>Spotlight is about finding things. Quicksilver is about finding things (faster) then <em>doing something</em> with what you&#8217;ve found.</li> 
	      			<li>Spotlight can show your dad&#8217;s entry in the Address Book. Quicksilver can show your dad&#8217;s work phone number, call it or send your dad a file.</li> 
	      			<li>Spotlight forces you to think as you search. Quicksilver allows you to just start typing to get what you want.</li> 

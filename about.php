	    			Using OS X's Spotlight or a similar app to find the contact 'John Smith' requires you to search for say &#8220;john&#8221;. What if you want to just type &#8220;js&#8221;? Quicksilver lets you abbreviate using letters from anywhere in the name, and that means anywhere. Use &#8220;ps&#8221; to find Photoshop or &#8220;tun&#8221; to find iTunes. <br />Use abbreviations that make sense to you.<br /> 
